#import "UIViewControllerDecorator.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation UIViewControllerDecorator

BKViewDecorateController(UIViewController)


////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)decorateButtons {
    [self decorateButtonWithIdentifier:@"default" block:^(UIButton *button) {
        [button setTitleColor:[UIColor redColor] forState:UIControlStateNormal];
    }];
}


////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)decorateLabels {
    [self decorateLabelWithIdentifier:@"default" block:^(UILabel *label) {
        label.textColor = [UIColor redColor];
    }];
}


////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)decorateViews {
    [self decorateViewWithIdentifier:@"background" block:^(UIView *view) {
        view.backgroundColor = [UIColor redColor];
    }];
}


@end
