#import "ExampleViewControllerDecorator.h"

#import "ExampleViewController.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation ExampleViewControllerDecorator

BKViewDecorateController(ExampleViewController)


////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)decorateViews {
    [self decorateViewWithIdentifier:@"background" block:^(UIView *view) {
        view.backgroundColor = [UIColor blueColor];
    }];
}


@end
